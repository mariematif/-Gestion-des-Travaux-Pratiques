    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        TP tp = (TP) listeTP.getSelectedItem();
        Etudiant etudiant = (Etudiant) listeETUDIANT.getSelectedItem();
        Date dateParticipation = txtdateparticipation.getDate();
        float note;
        try {
            note = Float.parseFloat(txtnote.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Veuillez entrer une note valide.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (ps.create(new ParticipationTP(tp, etudiant, dateParticipation, note))) {
            JOptionPane.showMessageDialog(this, "Bien enregistrée");
            load();
        } else {
            JOptionPane.showMessageDialog(this, "Problème");
        }


    }//GEN-LAST:event_bnAddActionPerformed


