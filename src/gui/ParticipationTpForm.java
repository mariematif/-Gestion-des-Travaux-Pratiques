    void loadTP() {
        for (TP t : ts.findAll()) {
            listeTP.addItem(t);

        }
    }

    void loadEtudiant() {
        for (Etudiant e : es.findAll()) {
            listeETUDIANT.addItem(e);
        }
    }

    void load() {
        model.setRowCount(0);
        for (ParticipationTP p : ps.findAll()) {
            model.addRow(new Object[]{
                p.getTp().getMatiere(),
                p.getEtudiant().getNom() + " " + p.getEtudiant().getPrenom(), p.getDateParticipation(), p.getNote(),});
        }
    }

